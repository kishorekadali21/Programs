package com.controlStatements;

public class IfElseIf {
	public static void main(String[] args) {
		
		int a = 0;
		
		if (a>0) {
			System.out.println("A is positive number");
		}else if(a<0) {
			System.out.println("A is negative number");
		}else {
			System.out.println("A is zero");
		}
	}
		

}
