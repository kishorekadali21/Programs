package com.Practise2;

public class ReplaceMultipleCharacters {
	public static void main(String[] args) {
        
	    String Str = new String("Welcome to Vtalent");
	          
	    System.out.print("After replacing all l with L : " );
	    System.out.println(Str.replace('l', 'L'));
	          
	    System.out.print("After replacing all e with E : " );
	    System.out.println(Str.replace('e', 'E'));
	          
	}

}
 